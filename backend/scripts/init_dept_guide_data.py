def init_dept_guide_data():
    """增量插入部门专属指引数据"""
    db = SessionLocal()
    try:
        existing_titles = {c.title for c in db.query(GuideCategory.title).all()}
        created_cats = 0
        created_items = 0

        max_sort = db.query(GuideCategory.sort_order).order_by(GuideCategory.sort_order.desc()).first()
        sort_base = (max_sort[0] + 1) if max_sort else 0

        for i, dept_data in enumerate(DEPARTMENT_GUIDE_DATA):
            if dept_data["title"] in existing_titles:
                print(f"  [SKIP] 分类已存在: {dept_data['title']}")
                continue

            category = GuideCategory(
                title=dept_data["title"],
                department_id=dept_data["department_id"],
                sort_order=sort_base + i,
            )
            db.add(category)
            db.flush()

            for j, item_data in enumerate(dept_data["items"]):
                item = GuideItem(
                    category_id=category.id,
                    question=item_data["question"],
                    answer=item_data["answer"],
                    sort_order=j,
                )
                db.add(item)
                created_items += 1

            created_cats += 1
            print(f"  [OK] 创建分类: {dept_data['title']} ({len(dept_data['items'])} 条)")

        db.commit()
        print(f"\n完成: 新增 {created_cats} 个分类, {created_items} 条条目")
    except Exception as e:
        db.rollback()
        print(f"失败: {e}")
    finally:
        db.close()


if __name__ == "__main__":
    init_dept_guide_data()
