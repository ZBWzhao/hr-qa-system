def init_guide_data():
    """初始化指引数据"""
    db = SessionLocal()
    try:
        # 检查是否已有数据
        existing_count = db.query(GuideCategory).count()
        if existing_count > 0:
            print(f"数据库中已有 {existing_count} 个分类，跳过初始化")
            return

        # 插入通用分类数据
        for i, cat_data in enumerate(INITIAL_DATA):
            category = GuideCategory(
                title=cat_data["title"],
                sort_order=i
            )
            db.add(category)
            db.flush()  # 获取ID

            for j, item_data in enumerate(cat_data["items"]):
                item = GuideItem(
                    category_id=category.id,
                    question=item_data["question"],
                    answer=item_data["answer"],
                    sort_order=j
                )
                db.add(item)

        # 插入部门专属分类数据
        for i, dept_data in enumerate(DEPARTMENT_GUIDE_DATA):
            category = GuideCategory(
                title=dept_data["title"],
                department_id=dept_data["department_id"],
                sort_order=len(INITIAL_DATA) + i
            )
            db.add(category)
            db.flush()

            for j, item_data in enumerate(dept_data["items"]):
                item = GuideItem(
                    category_id=category.id,
                    question=item_data["question"],
                    answer=item_data["answer"],
                    sort_order=j
                )
                db.add(item)

        db.commit()
        print(f"成功初始化 {len(INITIAL_DATA)} 个通用分类 + {len(DEPARTMENT_GUIDE_DATA)} 个部门专属分类的指引数据")
    except Exception as e:
        db.rollback()
        print(f"初始化失败: {e}")
    finally:
        db.close()


if __name__ == "__main__":
    init_guide_data()
